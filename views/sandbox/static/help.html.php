<p>
	This section will contain more of a manual in the future, but hopefully everything is straight forward. 
	In fact, if you're reading this then you should be pretty much set.
</p>
<p>
	The way this application works is simple. It's your playground. Just don't work off the master branch. 
	The first thing you'll want to do is make a new branch when playing around with your own stuff.
</p>
<p>
	Then, be sure to run git update every now and then on the master branch and feel free to merge those 
	changes into your own branches, but there shouldn't really be any major changes that you would need.
</p>
<p>
	The learning part comes in with the tutorials that you stumble upon that reference this sandbox 
	application. Various blog posts and screencasts may provide example code and that code can be found 
	in a branch. So you'll simply figure out which branch that is from the tutorial and check it out. 
</p>
<p>
	It's as simple as that really. Once you have the branch, you can follow along with the tutorial and 
	make changes to the code following along. When you want to follow along with another tutorial you 
	can check out a different branch, continue with the one you have, or start from a clean slate by 
	branching off the master branch. Whatever suits your situation best. 
</p>
<p>
	Make a mistake? No problem! You can remove the branch and check it out all over again to start over.
</p>
<p>
	The idea is that this sandbox should make learning fast and easy. It avoids the need to download some 
	zip file with example code or get it off a CD that may have come with a book, etc. It utilizes Git 
	and Github to make this an easy and collaborative learning environment.
</p>